    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
       try {
            Random rand;
            int  N = Integer.parseInt(numeroCirc.getText());
            int E = Integer.parseInt(espesor.getText());
            if (N > 20 || N < 3 || E > 20 || E < 3) {
                throw new MisException("1");
            }
            rand= new Random();
            rand.setSeed(new Date().getTime());
           int auxiliar1=rand.nextInt((int)(0.2*this.panel2Prin.getWidth()))+(int)(0.5*((double)E))+E;
            
            if((this.panel2Prin.getHeight()-auxiliar1-E)<(N*E*2)){
            
                throw new MisException("3");
            }
            
            dibujar(N,E,auxiliar1);
       }
       catch (MisException ex) {
            numeroCirc.setText(null);
            espesor.setText(null);
        }
       catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Ingrese valores enteros", "Error", JOptionPane.WARNING_MESSAGE);
            numeroCirc.setText(null);
            espesor.setText(null);
        }
         
    }//GEN-LAST:event_jButton5ActionPerformed

    
    void dibujar(int N,int E, int auxiliar1) {

        this.panel2Prin.removeAll();
        Ejercicio1Aux aux;
        aux = new Ejercicio1Aux(N, E, 2,auxiliar1);
        aux.setSize(this.panel2Prin.getWidth(), this.panel2Prin.getHeight());
        aux.setLocation(0, 0);
        aux.setBackground(Color.white);

        this.panel2Prin.add(aux, BorderLayout.CENTER);
        // this.revalidate(); //busca una validacion entre los metodos para verificar q todo sea correcto
        this.repaint(); // este actualiza lo q seria el dibujo
        //updateUI(); //este metodo es como q actualiza toda la interfaz
         aux.setBackground(Color.white);
    }

